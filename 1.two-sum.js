/**
 * @param {number[]} nums
 * @param {number} target
 * @return {number[]}
 */
let array = [];
let twoSum = function (nums, target) {
  let array = [];
  for (i = 0; i < nums.length; i++) {
    for (j = i + 1; j < nums.length; j++) {
      if (nums[i] + nums[j] == target) {
        array.push(i, j);
      }
    }
  }
  return array;
};
